
public class HelloWorld {

	public static void main(String[] args) {
		// 프로그램이 시작하면 가장 먼저 실행되는 함수 main

		// 컴파일: 번역가 (파파고). 파파고처럼 번역을 한다고 생각하면 됨
		// 번역은 한 번에 실행하고 결과까지 보여줌

		// 명령어:
		// 프로그램에서 결과를 확인할 때 사용하는 기능(함수) 출력
		// println() 괄호 안에 값을 넣음.

		// 숫자
		// 정수: 소수점이 없는 숫자 (int)
		System.out.println(1);
		System.out.println(-2);
		System.out.println(1000);

		// 실수: 소수점이 있는 숫자 (double, float)
		System.out.println(3.1415);
		System.out.println(3.14f);

		// 문자 여러개 (char)
		System.out.println('A');
		System.out.println('*');

		// 문자 여러개 (문자열 / String)
		System.out.println("안녕하세요!");
		System.out.println("오늘은 첫 날!");

		System.out.println('ㄱ');

	}

}
